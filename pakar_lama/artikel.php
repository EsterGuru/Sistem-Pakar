    <h4 class="media-heading"><b>Bagaimana Pencegahannya?</b></h4>

    <p align=justify>
   Pencegahan stunting dapat dilakukan dengan pemberian ASI eksklusif dan MPASI, memenuhi kebutuhan gizi dan protein, rutin melakukan pemeriksaan kesehatan difasilitas kesehatan, akses air bersih dan fasilitas sanitasi.

