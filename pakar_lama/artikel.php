  <div class="media-body">
    <h4 class="media-heading"><b>Apa Penyebab Stunting?</b></h4>

    <p align=justify>
    Penyebab stunting anatara lain yaitu asupan gizi dan status kesehatan yang meliputi ketahanan pangan (ketersediaan, keterjangkauan dan akses pangan bergizi), lingkungan sosial (norma, makanan bayi dan anak, hygiene, pendidikan, dan tempat kerja), lingkungan kesehatan (akses, pelayanan preventif dan kuratif), dan lingkungan pemukiman (air, sanitasi, kondisi bangunan). Penyebab tidak langsung masalah stunting dipengaruhi oleh beberapa faktor, meliputi pendapatan dan kesenjangan ekonomi, perdagangan urbanisasi, globalisasi, sistem pangan, jaminan sosial, sistem kesehatan, pembangunan pertanian, dan pemberdayaan perempuan.

<b><i></i></b></p>
  </div>

<div class="media">
  <a class="media-left" href="#">
  </a>
  <div class="media-body">
    <h4 class="media-heading"><b>Apa Saja Dampak Dari Stunting?</b></h4>

    <p align=justify>
    Dalam jangka pendek: Stunting menyebabkan gagal tumbuh, hambatan perkembangan kognitif dan motorik, dan tidak optimalnya ukuran fisik tubuh serta gangguan metabolisme.
    Dalam jangka panjang: Stunting menyebabkan menurunnya kapasitas intelektual. Gangguan struktur dan fungsi saraf dan sel-sel otak yang bersifat permanen dan menyebabkan penurunan kemampuan menyerap pelajaran di usia sekolah yang akan berpengaruh pada produktivitas saat dewasa. Selain itu kekurangan gizi juga menyebabkan gangguan pertumbuhan (pendek dan atau kurus) dan meningkatkan resiko penyakit tidak menular seperti diabetes mellitus, hipertensi, jantung koroner dan stroke.

<b><i></i></b></p>
  </div>

