<br><br> 
  </div>
<ol>
  <p>
<li>  Penggunaan Aplikasi Sistem Pakar Diagnosa Penyakit Stunting Pada Balita dengan browser yang telah terhubung server/internet. </li>
<li>  Menu login digunakan admin dan pakar untuk masuk kedalam sistem dan mengelolah data / pengetahuan berupa masukan data gejala, data relasi, dan bobot setiap gejala. </li>
<li>  Tahap pertama sebagai user yang telah masuk kedalam url sistem, harus melakukan pendaftaran terlebih dahulu, untuk selanjutnya diarahkan kedalam form pendaftaran. </li>
<li>  Setelah user mengisi identitas diri maka, langkah selanjutnya user klik tombol lanjutkan.</li>
<li>  User telah masuk kedalam form konsultasi, dan sistem akan menampilkan berbagai macam pertanyaan tentang gejala penyakit stunting sesuai dengan pengetahuan dan rule yang diinputkan oleh admin. </li>
<li>  User diminta menjawab pertanyaan yang disampaikan dengan mengklik tombol 'Ya' atau 'Tidak'.</li>
<li>  Setelah user menjawab semua pertanyaan yang diberikan, sistem akan mengarahkan user ke Form Hasil diagnosa, sehingga menampilkan riwayat pertanyaan, gejala yang dipilih, identitas diri, penyakit yang terdiagnosis, nilai keyakinan dengan persentase, dan solusi dari penyakit yang terdiagnosis oleh sistem. </li>
<li>  Langkah terakhir yaitu user dapat melakukan cetak hasil konsultasi dengan klik tombol cetak untuk print laporan.</li>
